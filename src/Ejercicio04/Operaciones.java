package Ejercicio04;

public class Operaciones {
    double n1;
    double n2;
    public double suma(){
        return n1+n2;
    }
    public double resta(){
        return n1-n2;
    }
    public double multiplicar(){
        return n1*n2;
    }
    public double dividir(){
        return n1/n2;
    }
}
